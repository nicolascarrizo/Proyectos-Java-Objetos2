package tp8;

public class Carta {
	
	private String palo;
	private Integer valor;
	
	public Carta(String palo, Integer valor) {
		super();
		this.palo = palo;
		this.valor = valor;
	}

	public String getPalo() {
		return palo;
	}

	public void setPalo(String palo) {
		this.palo = palo;
	}

	public Integer getValor() {
		return valor;
	}

	public void setValor(Integer valor) {
		this.valor = valor;
	}
	
	
	
	

}
